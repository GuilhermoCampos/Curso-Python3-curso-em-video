ent = input('Digite Algo: ')
print('O Tipo Primitivo desse item é = \033[30m', type(ent))
print('É um Numero? = \033[31m{}\033[m' .format(ent.isnumeric()))
print('É uma Letra? = \033[32m{}\033[m' .format(ent.isalpha()))
print('É Alfanumerico? = \033[33m{}\033[m' .format(ent.isalnum()))
print('É Maiúscula? = \033[34m{}\033[m' .format(ent.isupper()))
print('É Minúscula? = \033[35m{}\033[m' .format(ent.islower()))
print('É um espaço? = \033[36m{}\033[m' .format(ent.isspace()))
print('É um número decimal? = \033[37m{}\033[m' .format(ent.isdecimal()))
print('É capitaliza? = \033[7;30m{}\033[m' .format(ent.istitle()))
