n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
s = n1 + n2
d = n1 / n2
m = n1 * n2
e = n1 ** n2
di = n1 // n2
sub = n1 - n2
print('A soma é {}, A Divisão é {:.3f}, A multiplicação é {},'.format(s, d, m))
print('A Potênciação é {}, A Divisão inteira é {}, e a Subtração é {}.'.format(e, di, sub))
